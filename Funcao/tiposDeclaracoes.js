// Utilizada
// function declaration (função tradicional)
function soma(x, y) {
    return x + y;
}

// Utilizada
// function expression (função anônima)
const sub = function (x, y) {
    return x - y;
}

// Não mt utilizada
// named function expression (junção das duas anteriores)
const mult = function multi(x, y) {
    return x * y;
}