// Arrow function soma, recebe por parâmetro dois números e faz a soma.
const soma = (a, b) => a + b;
console.log(soma(2, 2));

// Arrow function subtração, recebe por parâmetro dois números e faz a subtração.
const subtracao = (c, d) => c - d;
console.log(subtracao(5, 3));

// Arrow function multiplicação, recebe por parâmetro dois números e faz a multiplicação.
const multiplicacao = (e, f) => e * f;
console.log(multiplicacao(5,5));

// Arrow function divisão, recebe por parâmetro dois números e faz a divisão.
const divisao = (g, h) => g * h;
console.log(divisao(6, 2));